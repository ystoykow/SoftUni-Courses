namespace _06._Reversed_Chars
{
    using System;

    class ReversedChars
    {
        static void Main()
        {
            string first = Console.ReadLine();
            string second = Console.ReadLine();
            string third = Console.ReadLine();
            string result = third + " " + second + " " + first + " ";
            Console.WriteLine(result);
        }
    }
}
